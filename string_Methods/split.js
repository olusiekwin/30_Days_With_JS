// split(): The split method splits a string at a specified place.
let string = "30 Days Of JavaScipt";
console.log(string.split()); // ["30 Days Of JavaScript"]
console.log(string.split(" ")); // ["30", "Days", "Of", "JavaScript"]
let firstName = "Graham";
console.log(firstName.split()); // ["Graham"]
console.log(firstName.split("")); // ["G", "r", "a", "h", "a", "m"]
let countries = "Kenya, Uganda, South Aftica, Nigeria, and Egypt";
console.log(countries.split(",")); // [ 'Kenya', ' Uganda', ' South Aftica', ' Nigeria', ' and Egypt' ]
console.log(countries.split(", ")); // [ 'Kenya', 'Uganda', 'South Aftica', 'Nigeria', 'and Egypt' ]
